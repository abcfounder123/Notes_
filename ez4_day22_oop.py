class Engine:
    def __init__(self, fuel_type):
        self.fuel_type = fuel_type
        self.state = "off"

    def on(self):
        if self.state == "off":
            self.state = "on"
            print(f"{self.fuel_type} Engine On.")
        else:
            print("Already On")

    def off(self):
        if self.state == "on":
            self.state = "off"
            print(f"{self.fuel_type} Engine Off.")
        else:
            print("Already Off")


